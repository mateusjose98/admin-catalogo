package org.dev.infrastructure.category.presenters;

import org.dev.application.category.retrieve.get.CategoryOutput;
import org.dev.application.category.retrieve.list.CategoryListOutput;
import org.dev.infrastructure.category.models.CategoryListResponse;
import org.dev.infrastructure.category.models.CategoryResponse;

public interface CategoryApiPresenter {

    static CategoryResponse present(final CategoryOutput output) {
        return new CategoryResponse(
                output.id().getValue(),
                output.name(),
                output.description(),
                output.isActive(),
                output.createdAt(),
                output.updatedAt(),
                output.deletedAt()
        );
    }

    static CategoryListResponse present(final CategoryListOutput output) {
        return new CategoryListResponse(
                output.id().getValue(),
                output.name(),
                output.description(),
                output.isActive(),
                output.createdAt(),
                output.deletedAt()
        );
    }
}